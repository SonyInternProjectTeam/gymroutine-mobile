              let repeatCount = goal.repeatCount else { return nil }
        
        let cycleInfo = goal.currentRepeatCycle != nil ? " (\(goal.currentRepeatCycle!)/\(repeatCount))" : ""
        
        switch repeatType {
        case "weekly":
            return "ÈÄ±Ê¨°ÂèçÂæ© \(repeatCount)Âõû\(cycleInfo)"
        case "monthly":
            return "ÊúàÊ¨°ÂèçÂæ© \(repeatCount)Âõû\(cycleInfo)"
